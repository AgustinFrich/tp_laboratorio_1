#ifndef ARITMETICA_H_
#define ARITMETICA_H_

/** \brief funcion que devuelve la suma de dos flotantes
 *
 * \param float primerNumero
 * \param float segundoNumero
 * \return float la suma de los dos parametros
 *
 */
float Suma(float, float);

/** \brief funcion que devuelve la diferencia de dos flotantes
 *
 * \param float primerNumero
 * \param float segundoNumero
 * \return float la sustraccion del segundo parametro del primero
 *
 */
float Resta(float, float);

/** \brief funcion que devuelve la division de dos flotantes
 *
 * \param float primerNumero
 * \param float segundoNumero
 * \return float la division del primer parametro por el segundo
 *
 */
float Division(float, float);

/** \brief funcion que devuelve la multiplicacion de dos flotantes
 *
 * \param float primerNumero
 * \param float segundoNumero
 * \return float la multiplicacion de los dos parametros
 *
 */
float Multiplicacion(float, float);

/** \brief funcion que devuelve el factorial de un numero
 *
 * \param float numero
 * \return float el factorial del numero ingresado
 *
 */
float Factorial(float);

#endif /* ARITMETICA_H_ */
